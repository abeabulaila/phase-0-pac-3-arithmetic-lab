function add (x, y){
         return x + y;
}

function subtract (a, b){
         return a - b;
}

function multiply (a, b){
        return a * b;
}

function divide (a, b){
         return a / b;
}

function increment(n) {
        return (n + 1);
}

function decrement(n) {
        return (n - 1);
}

function makeInt(n) {
    var result = parseInt(n, 10);
    return result;
}

function preserveDecimal(n) {
    var result = parseFloat(n);
    return result;
}